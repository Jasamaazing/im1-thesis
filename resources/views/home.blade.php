                    <p class="fst-italic">The best and most permanent way to replace real teeth is with dental implants. The placement of dental implants involves minor oral surgery to place the implant into the patient’s jawbone.</p>
                    <p>Though the process takes a few months and several procedures to complete, the placement of dental implants gives the patient permanent artificial teeth. Implant-supported crowns and dentures work just like real teeth.</p>
